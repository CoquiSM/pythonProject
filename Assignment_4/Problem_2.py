#This program assumes input variables are in cm

rectangleW1 = float(input("Enter Width of rectangle 1\n"))
rectangleH1 = float(input("Enter Height of rectangle 1\n"))
rectangleW2 = float(input("Enter Width of rectangle 2\n"))
rectangleH2 = float(input("Enter Height of rectangle 2\n"))

rectangleArea1 = rectangleW1 * rectangleH1
rectangleArea2 = rectangleW2 * rectangleH2

print("Area 1: " + str(rectangleArea1) + " centimeters squared")
print("Area 2: " + str(rectangleArea2) + " centimeters squared")




